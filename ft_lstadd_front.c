#include "libft.h"

/**
 * @brief Adds a node at the beginning of a list.
 * 
 * @param lst Address of a pointer to the first node.
 * @param new The node to add.
 */
void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (lst != NULL && new != NULL)
	{
		new->next = *lst;
		*lst = new;
	}
}
