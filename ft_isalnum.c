#include "libft.h"

/**
 * @brief Checks if a character is alphanumeric.
 * 
 * @param c The character to check.
 * @return 1 if alphanumeric, 0 otherwise.
 */
int	ft_isalnum(int c)
{
	if (ft_isalpha(c) || ft_isdigit(c))
		return (1);
	else
		return (0);
}
