#include "libft.h"

/**
 * @brief Outputs a string followed by a newline to a file descriptor.
 * 
 * @param s The string to output.
 * @param fd The file descriptor.
 */
void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}
