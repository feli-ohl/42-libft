#include "libft.h"

/**
 * @brief Iterates over a list and applies a function to each node.
 * 
 * @param lst The beginning of the list.
 * @param f The function to apply to each node's content.
 */
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst != NULL && f != NULL)
	{
		while (lst != NULL)
		{
			f(lst->content);
			lst = lst->next;
		}
	}
}
