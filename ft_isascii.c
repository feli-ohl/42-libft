/**
 * @brief Checks if a character is in the ASCII character set.
 * 
 * @param c The character to check.
 * @return 1 if ASCII character, 0 otherwise.
 */
int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	else
		return (0);
}
