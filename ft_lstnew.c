#include "libft.h"

/**
 * @brief Creates a new list node.
 * 
 * @param content The content to store in the node.
 * @return The newly allocated node, or NULL on failure.
 */
t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = malloc(sizeof(t_list));
	if (lst == NULL)
		return (NULL);
	lst->content = content;
	lst->next = NULL;
	return (lst);
}
